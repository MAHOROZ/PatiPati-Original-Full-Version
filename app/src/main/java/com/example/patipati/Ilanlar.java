package com.example.patipati;

public class Ilanlar {
    public String kategori, tur, cinsiyet, ilan_basligi,ilan_aciklamasi,yas;

    public Ilanlar() {
        this.kategori = kategori;
        this.tur = tur;
        this.cinsiyet = cinsiyet;
        this.ilan_basligi = ilan_basligi;
        this.ilan_aciklamasi = ilan_aciklamasi;
        this.yas = yas;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getTur() {
        return tur;
    }

    public void setTur(String tur) {
        this.tur = tur;
    }

    public String getCinsiyet() {
        return cinsiyet;
    }

    public void setCinsiyet(String cinsiyet) {
        this.cinsiyet = cinsiyet;
    }

    public String getIlan_basligi() {
        return ilan_basligi;
    }

    public void setIlan_basligi(String ilan_basligi) {
        this.ilan_basligi = ilan_basligi;
    }

    public String getIlan_aciklamasi() {
        return ilan_aciklamasi;
    }

    public void setIlan_aciklamasi(String ilan_aciklamasi) {
        this.ilan_aciklamasi = ilan_aciklamasi;
    }

    public String getYas() {
        return yas;
    }

    public void setYas(String yas) {
        this.yas = yas;
    }
}
